export const regex = {
  username: /^(?=.*[a-zA-Z])[a-zA-Z0-9_-]{3,36}$/,
  email:
    /^(?!\.)[a-zA-Z0-9_%+-]+(?:\.[a-z0-9_%+-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z]{2,4}$/,
  password:
    /^(?=.*[a-zA-Z])(?=.*[~!?@#$%^&*(){}[\]></\\|"'.,:_+-])[a-zA-Z0-9~!?@#$%^&*(){}[\]></\\|"'.,:_+-]{5,100}$/,
  loginOrEmail:
    /^(?=.*[a-zA-Z])[a-zA-Z0-9_-]{3,36}$|^(?!\.)[a-z0-9_%+-]+(?:\.[a-z0-9_%+-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z]{2,4}$/,
  fullName: /^[a-zA-Zа-яА-Я\- ]{0,100}$/,
};
